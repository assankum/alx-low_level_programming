/**
* print_alphabet_x10 - prints alphabets 10 times
*
* Description: prints  alphabet, in lowercase, 10 times followed by a new line.
*/

void print_alphabet_x10(void)
{
	int i;
	char c;

	for (i = 0; i <= 'z'; i++)
	{
		for (c = 'a'; a <= 'z'; a++)
			_putchar(a);
		_putchar('\n');
	}
}

